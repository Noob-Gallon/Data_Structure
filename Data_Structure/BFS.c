#include <stdio.h>
#include <stdlib.h>

#define TRUE 1
#define FALSE 0
#define MAX_QUEUE_SIZE 10

typedef int element; // -> 이건 왜 함?

typedef struct { // 큐 타입
	element  queue[MAX_QUEUE_SIZE]; 
	int  front, rear;
} QueueType;

// 오류 함수
void error(char* message)
{
	fprintf(stderr, "%s\n", message);
	exit(1);
}

// 큐 초기화
void queue_init(QueueType* q)
{
	q->front = q->rear = 0;
}

// 공백 상태 검출 함수
int is_empty(QueueType* q)
{
	return (q->front == q->rear); // front == rear
}

// 포화 상태 검출 함수
int is_full(QueueType* q)
{
	return ((q->rear + 1) % MAX_QUEUE_SIZE == q->front);
	// (rear+1) % Queue_Size == front, Circular Queue
}

// 삽입 함수
void enqueue(QueueType* q, element item)
{
	if (is_full(q))
		error("큐가 포화상태입니다");

	q->rear = (q->rear + 1) % MAX_QUEUE_SIZE;
	q->queue[q->rear] = item;
}

// 삭제 함수
element dequeue(QueueType* q)
{
	if (is_empty(q))
		error("큐가 공백상태입니다");
	q->front = (q->front + 1) % MAX_QUEUE_SIZE;
	return q->queue[q->front];
}


#define MAX_VERTICES 50
typedef struct GraphType {
	int n;	// 정점의 개수
	int adj_mat[MAX_VERTICES][MAX_VERTICES]; // 이건 그냥 graph?
} GraphType;

int visited[MAX_VERTICES]; // visited

// 그래프 초기화 
void graph_init(GraphType* g)
{
	g->n = 0;

	// 그래프를 모두 0으로 초기화.
	for (int i = 0; i < MAX_VERTICES; i++)
		for (int j = 0; j < MAX_VERTICES; j++)
			g->adj_mat[i][j] = 0;
}
// 정점 삽입 연산
void insert_vertex(GraphType* g, int v)
{
	if (((g->n) + 1) > MAX_VERTICES) {
		fprintf(stderr, "그래프: 정점의 개수 초과");
		return;
	}
	g->n++;
}
// 간선 삽입 연산
void insert_edge(GraphType* g, int start, int end)
{
	if (start >= g->n || end >= g->n) {
		fprintf(stderr, "그래프: 정점 번호 오류");
		return;
	}

	g->adj_mat[start][end] = 1;
	g->adj_mat[end][start] = 1;
}
void bfs_mat(GraphType* g, int v)
{
	int w;
	QueueType q;

	queue_init(&q);     // 큐 초기화
	visited[v] = TRUE;          // 정점 v 방문 표시 
	printf("%d  방문 -> ", v);
	enqueue(&q, v);			// 시작 정점을 큐에 저장 

	while (!is_empty(&q)) { 
		// 큐가 비어있지 않으면 반복문을 실행

		v = dequeue(&q);		// 큐에 정점 추출 

		for (w = 0; w < g->n; w++)	// 인접 정점 탐색
		{
			if (g->adj_mat[v][w] && !visited[w])
				// dequeue한 vertex v와 연결되어 있고,
				// 그 vertex가 방문하지 않은 vertex라면,
			{
				visited[w] = TRUE;    // 방문 표시
				printf("%d 방문 -> ", w);
				enqueue(&q, w); 	// 방문한 정점을 큐에 저장
			}
		}
	}
}

int main(void)
{
	GraphType* g;
	// GraphType은 Vertex의 개수와 graph를 갖는 구조체
	// 동적할당을 위해 변수를 선언해준다.

	g = (GraphType*)malloc(sizeof(GraphType));

	// GraphType g의 초기화
	graph_init(g);

	// vertex 개수 추가
	for (int i = 0; i < 6; i++)
		insert_vertex(g, i);

	// vertex간의 Edege 추가(Undirected)
	insert_edge(g, 0, 2);
	insert_edge(g, 2, 1);
	insert_edge(g, 2, 3);
	insert_edge(g, 0, 4);
	insert_edge(g, 4, 5);
	insert_edge(g, 1, 5);

	bfs_mat(g, 0);
	printf("\n");

	free(g);
	return 0;
}